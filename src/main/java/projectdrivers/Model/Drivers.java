package projectdrivers.Model;

public class Drivers {
    private String nameDriver;
    private float pointsDriver;

    public Drivers(String nameDriver, float pointsDriver) {
        this.nameDriver = nameDriver;
        this.pointsDriver = pointsDriver;
    }

    public String getNameDriver() {
        return nameDriver;
    }

    public void setNameDriver(String nameDriver) {
        this.nameDriver = nameDriver;
    }

    public float getPointsDriver() {
        return pointsDriver;
    }

    public void setPointsDriver(float pointsDriver) {
        this.pointsDriver = pointsDriver;
    }

    
    
}
